        $minValidDate = DB::connection('mysql_lama')
            ->table('logpenghuni')
            ->where('Tanggal', '!=', '0000-00-00')
            ->min('Tanggal');

        // Fallback jika tidak ada yang valid
        if (!$minValidDate) {
            $minValidDate = '1970-01-01';
        }

        // Simpan log ke baru
        foreach ($logLama as $log) {
            $idPenghuni = (int) $log->ID_Penghuni;
            $idKamar = (int) $log->ID_Kamar;

            // Lewati jika mapping tidak ditemukan
            if (!isset($penghuni_mapping[$idPenghuni]) || !isset($kamarMapping[$idKamar])) {
                continue;
            }

            $uuidPenghuni = $penghuni_mapping[$idPenghuni];
            $uuidKamar = $kamarMapping[$idKamar];
            $tanggal = trim($log->Tanggal) === '0000-00-00' ? $minValidDate : $log->Tanggal;

            // Konversi tanggal
            // $tanggal = Carbon::createFromFormat('d/m/Y', $log->Tanggal)->format('Y-m-d');

            $statusNormalized = strtolower(str_replace(' ', '', trim($log->Status)));

            // Insert ke log_penghunis baru
            DB::connection('mysql')->table('log_penghunis')->insert([
                'id' => Str::uuid(), // tambahkan baris ini!
                'penghuni_id' => $uuidPenghuni,
                'kamar_id' => $uuidKamar,
                'tanggal' => $tanggal,
                'status' => $statusNormalized,
                'created_at' => now(),
                'updated_at' => now(),
            ]);
        }

        // Ambil status terakhir untuk tiap kamar
        $statusTerakhir = DB::connection('mysql')->table('log_penghunis')
            ->select('kamar_id', DB::raw('MAX(tanggal) as max_tanggal'))
            ->groupBy('kamar_id')->get();

        foreach ($statusTerakhir as $item) {
            $lastStatus = DB::connection('mysql')->table('log_penghunis')
                ->where('kamar_id', $item->kamar_id)
                ->where('tanggal', $item->max_tanggal)
                ->orderByDesc('created_at')
                ->first();

            if (!$lastStatus) continue;

            $statusKamar = strtolower($lastStatus->status) === 'checkin' ? 'terisi' : 'kosong';

            DB::connection('mysql')->table('ketersediaan_kamars')->updateOrInsert(
                ['kamar_id' => $item->kamar_id],
                [
                    'id' => Str::uuid(),
                    'status' => $statusKamar,
                    'updated_at' => now(),
                ]
            );
        }

        echo "Migrasi log_penghunis selesai.\n";
    }
